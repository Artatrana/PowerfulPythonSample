# 5. Proxy Pattern
# Intent: Provide a placeholder or intermediary to control access to another object.
# Real-world Analogy: A security guard controlling access to a building
# Real Applications
# - API Gateway(Nginx, Kong)
# - ORM Lazy Loading ( Hibernate, SQLAlchemy)
# When to Use
# - Access control
# - Lazy initialization
# - Caching or logging
