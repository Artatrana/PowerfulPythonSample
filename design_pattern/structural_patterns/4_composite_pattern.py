# 4. Composite Pattern
# Intent: Treat individual objects and compositions of objects uniformly.
# Real-world Analogy: A folder that can contain files and other folders - both can be copied, moved, and deleted the same way.
# Real Application:
# - File systems
# - UI Frameworks ( React Component trees)

# When to Use
# Tree‑like structures
# Hierarchical data
# Part‑whole relationships